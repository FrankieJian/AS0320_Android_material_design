public class MaterialDesign {
    private String name;
    private int imageId;

    public MaterialDesign(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
